# OPERADORES DE COMPARAÇÃO

'''UTILIZADOS PARA COMPARAR DOIS VALORES'''



saldo = 450
saque = 200

# IGUALDADE ==

print(saldo == saque) # igual? False

# DIFERENÇA !=

print(saldo != saque) # diferente? True

# MAIOR/IGUAL <=

print(saldo > saque) # maior? True

print(saldo >= saque) # maior ou igual? True

# MENOR/IGUAL <=

print(saldo < saque) # menor? True

print(saldo <= saque) # menor ou igual? True

 
# AULA - OPERADORES ARITIMÉTICOS E ORDEM DE PRECEDÊNCIA

produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2) #ADIÇÃO
print(produto_1 - produto_2) #SUBTRAÇÃO
print(produto_1 / produto_2) #DIVISÃO
print(produto_1 // produto_2) #MÓDULO 
print(produto_1 * produto_2) #MULTIPLICAÇÃO
print(produto_1 % produto_2) #DIVISÃO INTEIRA OU RESTO DA DIVISÃO
print(produto_1 ** produto_2) #EXPONENCIAÇÃO

x = 10 + 5 * 4
print(x)

x = (10 + 5) * 4
print(x)

y = 10 / 2 + (25 * 2 - (2 ** 2)) # é uma bos prática sempre deixar explicito a ordem de procedência por meio de parêntesis.
print(y)

y = (10 / 2) + 25 * ((2 - 2) ** 2)
print(y)


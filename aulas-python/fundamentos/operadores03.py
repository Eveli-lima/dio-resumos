# OPERADORES DE ATRIBUIÇÃO

'''UTILIZADOS PARA DEFINIR O VALOR OU SOBRESCREVER O VALOR DE UMA VARIÁVEL.'''

saldo = 500
saldo += 200 # É uma forma reduzida de escrever saldo = saldo + 200
print(saldo)

saldo = 500
saldo -= 100 # saldo = saldo - 100 ou seja saldo recebe 500 - 100 = 400
print(saldo)

saldo = 500
saldo *= 2 # saldo recebe saldo que é 500 vezes * 2 = 1000
print(saldo)

saldo = 500
saldo /= 5 # saldo recebe saldo que é 500 divido / 2 = 100.0
print(saldo)

saldo = 500
saldo //= 5 # saldo recebe saldo que é 500 e a divisão inteira // (que é o mesmo resultado da divisão sem o ponto flutuante) = 100
print(saldo)

saldo = 500
saldo %= 480 # saldo recebe saldo que é 500 módulo de % 480 = 20
print(saldo)

saldo = 80
saldo **= 2 # saldo recebe saldo que é 80 elevado a 2 = 6400
print(saldo)


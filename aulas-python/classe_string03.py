# FATIAMENTO DE STRINGS

'''É UMA TÉCNICA UTILIZADA PARA RETORNAR SUBSTRINGS (PARTES DA STRING ORIGINAL), INFORMANDO INICIO (START), FIM (STOP) E PASSOS (STEP): [START: STOP[, STEP]]'''

nome = "Éveli Azevedo de Oliveira Lima"

print(nome)

print(nome[0])

print(nome[:5])  

print(nome[-2]) # POSSO COLOCAR NÚMEROS, NESSE CASO VAI EXIBIR O M DE LIMA

print(nome[17:])

print(nome[6:13])

print(nome[6:13:2])

print(nome[:])

print(nome[::-1]) 
